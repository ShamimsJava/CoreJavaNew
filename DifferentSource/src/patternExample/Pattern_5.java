package patternExample;

public class Pattern_5 {
    public static void main(String[] args) {
        char i;
        char j;
        for (i = 'A';  i <= 'E'; i++) {
            for(j = 'A'; j <= 'E'; j++){
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
